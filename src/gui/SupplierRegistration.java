    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        String name = jTextField1.getText();
        String company = jComboBox1.getSelectedItem().toString();
        String cnumber = jTextField2.getText();
        String email = jTextField3.getText().toLowerCase();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Supplier Name", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (company.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select Company Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("0[0-9]{9}").matcher(cnumber).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Mobile number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("^[a-z0-9._%+-]+@[a-z0-9.-]+\\.[a-z]{2,6}$").matcher(email).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid E-mail address", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs1 = MySQL.search("SELECT `id` FROM `company` WHERE `company`='" + company + "'");
                rs1.next();

                String company_id = rs1.getString("id");
                
//                ResultSet rs = MySQL.search("SELECT * FROM `supplier` WHERE `email`='" + email + "' OR `contact_number`='" + cnumber + "';");
//
//                if (rs.next()) {
//                    JOptionPane.showMessageDialog(this, "Supplier already exists", "Warning", JOptionPane.WARNING_MESSAGE);
//                } else {

                    MySQL.iud("INSERT INTO `supplier`(`name`,`company_id`,`contact_no`,`email`) VALUES('" + name + "'," + Integer.parseInt(company_id) + ",'" + cnumber + "','" + email + "')");

                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jComboBox1.setSelectedIndex(0);

                    loadSupplier();
                    jTextField1.grabFocus();

                    JOptionPane.showMessageDialog(this, "New supplier account created", "Success", JOptionPane.INFORMATION_MESSAGE);

//                }  

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton2ActionPerformed

