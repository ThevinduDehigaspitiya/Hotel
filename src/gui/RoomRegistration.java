    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String roomno = jTextField1.getText();
        String bedcount = jTextField2.getText();
        String type = jComboBox1.getSelectedItem().toString();

        if (roomno.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Room Number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (bedcount.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Bed Count", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (type.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select Room Type", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs1 = MySQL.search("SELECT `id` FROM `rooms_type` WHERE `type`='" + type + "'");
                rs1.next();

                String type_id = rs1.getString("id");
                

                MySQL.iud("INSERT INTO `rooms`(`room_no`,`bed_count`,`type_id`) VALUES('" + roomno + "','" + bedcount + "'," + Integer.parseInt(type_id) + ")");

                resetFields();

                JOptionPane.showMessageDialog(this, "New Room Register", "Success", JOptionPane.INFORMATION_MESSAGE);

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

